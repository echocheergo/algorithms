package javasrc;

public class UberValidParentheses {
    // use a stack or virtual stack to sotre left ({[
    // only way to remove from stack is right bracket + last char is corresponding left bracket
    // check status as stack should be empty at end
    public boolean isValid(String s) {
        char[] stack = new char[s.length()];
        int head = 0;
        for(char c : s.toCharArray()) {
            switch(c) {
                case '{':
                case '[':
                case '(':
                    stack[head++] = c;
                    break;
                case '}':
                    if(head == 0 || stack[--head] != '{') return false;
                    break;
                case ')':
                    if(head == 0 || stack[--head] != '(') return false;
                    break;
                case ']':
                    if(head == 0 || stack[--head] != '[') return false;
                    break;
            }
        }
        return head == 0;

    }
}
